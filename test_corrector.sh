#!/bin/bash
# =============================================================================
# Тестирование контейнера коррекции
# =============================================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
TEST_DIR="$SCRIPT_DIR/test"
OUTPUT_DIR="/home/ubuntu/output"

# ────────────────────────────────────────────────
# Подготовка
# ────────────────────────────────────────────────
echo "=== Создание тестовой директории ==="
mkdir -p "$TEST_DIR"
mkdir -p "$OUTPUT_DIR"

# ────────────────────────────────────────────────
# Создание тестового файла с ошибками
# ────────────────────────────────────────────────
echo "=== Создание тестового отчёта с ошибками ==="
cat > "$OUTPUT_DIR/test_report.md" << 'TESTEOF'
# ОТЧЕТ ПО ИТОГАМ СОВЕЩАНИЯ

## ОБЩАЯ ИНФОРМАЦИЯ
- **Аудиофайл:** `meeting_2024.wav`
- **Длительность:** 45 мин 30 сек
- **Участников:** 5

## КРАТКОЕ СОДЕРЖАНИЕ
На совещании обсудили план развтия проекта. Было принято решение о запуске новой фичи в следущем квартале.

## ПРИНЯТЫЕ РЕШЕНИЯ
- Решение: Запустить новую фичу до конца квартала (00:15:30)
- Решение: Выдилить дополнительный бюджет на маркетинг (00:23:45)

## ЗАДАЧИ
| Задача | Ответственный | Дедлайн | Статус |
|:---|:---|:---|:---|
| Подготовить ТЗ | Иванов | 27.12 | В процессе |
| Согласовать с заказчиком | Петров | 30.12 | Не начато |

## КЛЮЧЕВЫЕ ПУНКТЫ ОБСУЖДЕНИЯ
1. Бюджет проекта (00:05:00): обсудили выделение доп средств
2. Сроки реализации (00:12:30): сдвиг на 2 недели

## АНАЛИТИЧЕСКАЯ СВОДКА
Основная проблема - нехватка ресурсов в отделе разработки. Рекомендуется нанять ещё двух разработчиков.

## СТАТИСТИКА УЧАСТНИКОВ
- **Спикер_00**: 45 реплик, 1250 слов
- **Спикер_01**: 32 реплик, 890 слов
TESTEOF

echo "✅ Тестовый файл создан: $OUTPUT_DIR/test_report.md"

# ────────────────────────────────────────────────
# Проверка llama-server
# ────────────────────────────────────────────────
echo ""
echo "=== Проверка llama-server ==="
if curl -s --max-time 5 "http://127.0.0.1:8080/health" > /dev/null 2>&1; then
    echo "✅ llama-server доступен"
else
    echo "❌ llama-server недоступен"
    echo "Запустите: sudo systemctl start llama-server.service"
    exit 1
fi

# ────────────────────────────────────────────────
# Сборка образа
# ────────────────────────────────────────────────
echo ""
echo "=== Сборка Docker образа ==="
cd "$SCRIPT_DIR"
docker compose build corrector

# ────────────────────────────────────────────────
# Запуск коррекции
# ────────────────────────────────────────────────
echo ""
echo "=== Запуск коррекции ==="
./run_corrector.sh "$OUTPUT_DIR/test_report.md" "$OUTPUT_DIR/test_report_corrected.md"

# ────────────────────────────────────────────────
# Результаты
# ────────────────────────────────────────────────
echo ""
echo "=== Результаты ==="
if [ -f "$OUTPUT_DIR/test_report_corrected.md" ]; then
    echo "✅ Исправленный файл создан"
    echo ""
    echo "=== Различия (оригинал → исправлено) ==="
    diff --color=always -u "$OUTPUT_DIR/test_report.md" "$OUTPUT_DIR/test_report_corrected.md" || true
    echo ""
    echo "✅ Тестирование завершено успешно"
else
    echo "❌ Исправленный файл не создан"
    exit 1
fi
